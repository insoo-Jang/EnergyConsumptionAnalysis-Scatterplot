export const TimeRangeColor = (param) => {
    switch (param) {
        case '01':
            return 'blue'
        case '02':
            return 'blue'
        case '03':
            return 'blue'
        case '04':
            return 'yellow'
        case '05':
            return 'yellow'
        case '06':
            return 'red'
        case '07':
            return 'red'
        case '08':
            return 'red'
        case '09':
            return 'red'
        case '10':
            return 'yellow'
        case '11':
            return 'blue'
        case '12':
            return 'blue'
        default:
            return 'black'
    }
}
